/*arithmetic operators*/
#include <stdio.h>

int main() 
{
    int a=20, b=10, add, sub, mult, div, mod;
    add=a+b;
    sub=a-b;
    mult=a*b;
    div=a/b;
    mod=a%b;
    printf("results of addition=%d\n", add);
    printf("results of subtraction=%d\n", sub);
    printf("results of multiplication=%d\n", mult);
    printf("results of division=%d\n", div);
    printf("results of modulus=%d\n", mod);
    return 0;
}

    
 
